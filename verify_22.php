                   <div id="part4">
                       
                       <p style="text-align:center"><span style="font-size:22px;"></span>第四章 &nbsp; 中国胸痛中心认证标准
                       </p>
                       <p align="center" style="line-height:150%;text-align:center"><span style="font:Simhei;">中国胸痛中心认证工作委员会，                    <span lang="en">2015</span>年<span lang="en" >11</span>
                             月修订）                       
                       </span>
                       </p>
                       <p>&nbsp;
                           
                       </p>
                       <p style="line-height:150%;">
                           &nbsp;&nbsp;&nbsp;&nbsp;
                           胸痛中心的建设目标是要建立“在最短的时间内将急性胸痛患者送至具有救治能力的医院接受最佳治疗”的机制，通过对我国急性心肌梗死救治现状的分析，中国胸痛中心认证工作委员会所确立的我国胸痛中心建设的基本理念是：以具备直接经皮冠状动脉介入治疗（PPCI）能力的医院为核心，通过对医疗资源的整合建立起区域协同快速救治体系，以提高急性胸痛患者的整体救治水平。为实现此目标，中国胸痛中心认证标准共包含五大要素，分别是：基本条件与资质、对急性胸痛患者的评估和救治、院前急救系统与院内绿色通道的整合、培训与教育、持续改进。
                       </p>
                       <p align="center">
                           <span style="text-align:center; font-size:20px;font-family:Simsun">要素一&nbsp;&nbsp;基本条件与资质</span>
                       </p>
                       <p style="padding-left:30px"><span style="font-size:12px">胸痛中心申请认证之前必须满足此要素的全部条件。</span></p>
                       <p style="font-family:nSimSun ;color:#111111;font-weight:bold">一、胸痛中心的组织机构（10分）</p>
                       <p><span style="color:black;">由于胸痛中心是通过整合院内外相关优势技术和力量为急性胸痛患者提供快速诊疗通道的机构，既可以是在不改变现有结构基础之上实体运作的虚拟机构，也可以是重新组建的实体机构。但不论何种方式，胸痛中心的建设均要涉及到医院内外许多部门，必须有一套相应的组织机构进行协调和管理。组织机构的形式可以因不同医院的实际情况而定，但基本要求和任务是相同的。</span></p>
                       
                       <p>1、医院发布正式文件成立胸痛中心及胸痛中心委员会，要求：</p>
                       <p>1）由医院院长或分管医疗的副院长担任胸痛中心委员会主任委员，主持胸痛中心委员会的建设和重大决策；（2分，资料）（1.10）</p>
                       <p>2）书面文件明确胸痛中心委员会的工作职责；（1分，资料）（1.11）</p>
                       <p>3） 明确胸痛中心委员会具有调动医院所有资源为胸痛中心建设和运行提供保障的权力；（1分，资料）（1.12）</p>
                       <p>4）胸痛中心成立并实际运作至少6个月以上才能申请认证。（1分，资料）（1.13）</p>
                       <p> <strong><span style="color:blue">说明：1.10-1.13 需上传医院正式文件的扫描件。</span></strong></p>
                       <p><span style="color:black;font-family:NSimSun">2、 任命胸痛中心医疗总监,要求：</span></p>
                       <p>1）医院正式任命一名具有心血管内科专业背景的高级职称医师担任胸痛中心医疗总监，且该医师应具备较强的组织协调能力，专业技能必须具备对急性冠状动脉综合征（ACS）、急性主动脉夹层、肺动脉栓塞等急性胸痛患者进行诊断、鉴别诊断及紧急救治能力；（1分，资料）（1.14）</p>
                       <p><span>2）正式文件明确胸痛中心医疗总监的职责；（1分，资料）（1.15）</span></p>
                       <p><strong><span style="color:blue">说明：1.14-1.15需上传以下
                       材料：</span></strong></p>
                       
                       <p><strong><span style="color:blue">1、医疗总监任命文件的扫描件</span></strong></p>
                       <p><strong><span style="color:blue">2、明确医疗总监职责的正式文件</span></strong></p>
                       <p><strong><span style="color:blue">3、医疗总监的专业资质文件：资格证书和职称证书</span></strong></p>
                       
                       <p style="line-height:150%;text-indent:21pt">
                           <strong><span style="color=black">3、任命胸痛中心协调员，要求：</span></strong>
                           
                       </p>
                       <p style="line-height:150%">
                           <span>1）指定一名具有急诊或心血管内科专业背景的医师担任胸痛中心协调员，协调员必须具备正确处理ACS及其它急性胸痛的能力；（1分，资料）（1.16）</span>
                       </p>
                       <p style="line-height:150%">
                           <span>2）书面文件明确协调员的具体工作职责；（1分，资料）（1.17）</span>
                       </p>                       
                       <p style="line-height:150%">
                           <span> 3）协调员每年至少参加ACS和胸痛中心相关的培训不少于10学时。（1分，资料）（1.18）</span>
                       </p>                         
