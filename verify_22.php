               <div id="aims">
               	<span style="font-weight:bold;color:#669900;" title="title"> <p style="text-align:center">
                    认证的目的及意义
                    </p> 
                </span> 
                <br>
                &nbsp;&nbsp;&nbsp;&nbsp;胸痛中心是为降低急性心肌梗死的发病率和死亡率提出的概念，通过多学科（包括急救医疗系统、急诊科、心内科和影像学科等）合作，提供快速而准确的诊断、危险评估和恰当的治疗手段，从而提高早期诊断和治疗急性冠状动脉综合征（ACS）的能力，降低心肌梗死发生的可能性或者减少心肌梗死面积，并准确筛查出心肌缺血低危患者，达到减少误诊和漏诊及过度治疗，以改善患者临床预后的目的。全球第一家“胸痛中心”于1981年在美国巴尔地（的）摩St.ANGLE医院建立，至今美国“胸痛中心”已经发展到5000余家。目前全球多个国家如英国、法国、加拿大、澳大利亚、德国等国家在医院内设立有“胸痛中心”。美国胸痛中心协会（SCPC）通过对各胸痛中心的认证工作大大推动了美国胸痛中心的标准化进程，推动了专业指南尤其是ACS相关指南在临床实践中的落实，使美国对ACS的整体急救水平大大提高。除美国之外，德国心血管病学会也启动了德国胸痛单元的认证工作，德国胸痛单元的认证对推动全德甚至部分欧洲其它国家的ACS的救治水平发挥了及其重要的作用。
                <br><br>
                &nbsp;&nbsp;&nbsp;&nbsp;我国的ACS救治的总体水平与发达国家及指南的要求仍有很大的差距，主要体现在患者发病后就诊时间延误和就诊以后医疗系统内的延误过长，使许多患者错过了最佳救治时间，从而导致ACS的死亡率较高和长期预后较差，这与我国尚未广泛开展胸痛中心的建设有着密切的联系。2010年发表的《胸痛中心建设中国专家共识》和2011年由国家卫计委发起的《中国急性心肌梗死规范化救治项目》对我国胸痛中心的建设起到了积极的推动作用，全国胸痛中心建设呈现良好的发展势头。但总体来看，我国的胸痛中心建设刚刚起步，各家胸痛中心的运作模式、管理机制和实际运作效果差别很大,其中很重要的原因是我国还没有自己的认证标准，多数是按照自己对美国胸痛中心的认识结合自身条件而设计。为使我国的胸痛中心建设达到规范化要求，急需建立中国自己的认证标准。为此，由中华医学会心血管病分会牵头制订中国胸痛中心认证标准，以指导全国胸痛中心的建设和发展。积极参加中国胸痛中心自主认证工作，也是各医疗机构按照标准流程进行自我评估和对照检查的绝好契机，有利于扩大医疗机构的影响力，有利于促进临床实践和医疗管理向规范化、系统化、流程化、标准化迈进。也是规范我国急性心肌梗死诊疗行为、提高急性心肌梗死、主动脉夹层、肺动脉栓塞等急性胸痛相关疾病的诊疗水平、缩短与国际先进水平之间差距的重要措施。
